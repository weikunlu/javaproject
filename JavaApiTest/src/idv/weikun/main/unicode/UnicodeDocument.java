public class UnicodeDocument {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		//output 24171
		System.out.println((int)'幫');
		
		//output 5e6b
		System.out.println(Integer.toHexString(24171));
		
		//output 5e6b
		System.out.println(Integer.toHexString('幫'));
		
		
		System.out.println("\u90A6 \u5e6b \u5fd9  \u87CE");  

		new UnicodeDocument().travelUnicodeBetweenChars();
	}

	void travelUnicodeBetweenChars(){
		
		//2E80-2FDF, 3400-4DBF, 4E00-9FFF
		char startChar = '\uE7C7';
		char endChar = '\uE7F3';
		int i;
		for (i=0;startChar!=endChar;++i,startChar++){
			System.out.println(startChar);
		}
		System.out.println("i: "+i);
		
	}
	
	
}
